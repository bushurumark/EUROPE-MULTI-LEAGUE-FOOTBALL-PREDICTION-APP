import streamlit as st

# helpers.py
def initialize_app():
    """
    Set up the Streamlit page configuration and apply custom styles.
    """
    st.set_page_config(page_title="Football Predictor", layout="centered")
    try:
        with open("style.css") as f:
            st.markdown(f"<style>{f.read()}</style>", unsafe_allow_html=True)
    except FileNotFoundError:
        st.warning("⚠️ style.css not found. Using default styling.")

    st.markdown('<div class="title">⚽ FOOTBALL PREDICTION APP</div>', unsafe_allow_html=True)

def initialize_session():
    """
    Initialize prediction state in Streamlit session.
    """
    if "prediction_made" not in st.session_state:
        st.session_state.prediction_made = False

def get_user_input(leagues):
    """
    Create dropdowns for selecting match details from league data.

    Args:
        leagues (dict): Nested dictionary of available categories, leagues, and teams.

    Returns:
        tuple: (category, league, home_team, away_team)
    """
    category = st.selectbox("Select Category", list(leagues.keys()))
    league = st.selectbox("Select a League", list(leagues[category].keys()))
    teams = leagues[category][league]
    home_team = st.selectbox("Select Home Team", teams)
    away_team = st.selectbox("Select Away Team", teams)
    return category, league, home_team, away_team