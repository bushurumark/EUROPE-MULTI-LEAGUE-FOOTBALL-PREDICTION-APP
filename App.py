# Load model and data
model = download_model()
data = load_data()

# Function to compute the mean of head-to-head stats for the selected teams
def compute_mean_for_teams(home_team, away_team):
    h2h_data = data[(data['HomeTeam'] == home_team) & (data['AwayTeam'] == away_team)]
    if h2h_data.empty:
        st.error(f"No historical data available with {home_team} as the home team and {away_team} as the away team.")
        return None

    h2h_data = h2h_data.drop(columns=['FTR', 'Date', 'HomeTeam', 'AwayTeam'], errors='ignore')
    h2h_data['HTR'] = h2h_data['HTR'].replace({'H': 1, 'D': 2, 'A': 3})
    mean_data = h2h_data.mean(numeric_only=True)

    if 'HTR' in mean_data:
        if 0 <= mean_data['HTR'] <= 1.4:
            mean_data['HTR'] = 'H'
        elif 1.5 <= mean_data['HTR'] <= 2.4:
            mean_data['HTR'] = 'D'
        elif 2.5 <= mean_data['HTR'] <= 3.4:
            mean_data['HTR'] = 'A'

    input_data = pd.DataFrame([mean_data])
    model_features = model.feature_names_in_
    for feature in model_features:
        if feature not in input_data.columns:
            input_data[feature] = 0
    input_data = input_data[model_features]

    return input_data

# Function to calculate win probabilities based on historical data
def calculate_probabilities(home_team, away_team):
    # Filter historical matches
    h2h_data = data[(data['HomeTeam'] == home_team) & (data['AwayTeam'] == away_team)]

    if h2h_data.empty:
        st.error(f"No historical data available with {home_team} as the home team and {away_team} as the away team.")
        return None

    # Count outcomes
    total_matches = len(h2h_data)
    home_wins = (h2h_data['FTR'] == 'H').sum()  # Home team wins
    draws = (h2h_data['FTR'] == 'D').sum()      # Draws
    away_wins = (h2h_data['FTR'] == 'A').sum()  # Away team wins

    # Calculate probabilities
    home_win_prob = (home_wins / total_matches) * 100 if total_matches > 0 else 0
    draw_prob = (draws / total_matches) * 100 if total_matches > 0 else 0
    away_win_prob = (away_wins / total_matches) * 100 if total_matches > 0 else 0

    return {
        "Home Team Win": home_win_prob,
        "Draw": draw_prob,
        "Away Team Win": away_win_prob
    }


# Prediction function with Plotly interactive visualization
def predict_with_h2h_data(home_team, away_team):
    # Historical probabilities
    probabilities = calculate_probabilities(home_team, away_team)
    if probabilities is None:
        return

    # Model-based prediction
    input_data = compute_mean_for_teams(home_team, away_team)
    if input_data is None:
        return

    prediction = model.predict(input_data)[0]

    # Colors for the bars and results
    colors = {
        "Home Team Win": "green",
        "Draw": "yellow",
        "Away Team Win": "red"
    }

    # Plot the prediction probabilities using Plotly
    fig = px.bar(
        x=["Home Team Win", "Draw", "Away Team Win"],
        y=[probabilities["Home Team Win"], probabilities["Draw"], probabilities["Away Team Win"]],
        labels={'x': 'Outcome', 'y': 'Probability (%)'},
        title="Match Outcome Probabilities",
        color=["Home Team Win", "Draw", "Away Team Win"],
        color_discrete_map=colors
    )
    st.plotly_chart(fig)

    # Display the prediction
    if 0.5 <= prediction <= 1.4:
        st.markdown('<div class="prediction-result">üèÜ Prediction: Home Team Win</div>', unsafe_allow_html=True)
    elif 1.5 <= prediction <= 2.4:
        st.markdown('<div class="prediction-result">ü§ù Prediction: Draw</div>', unsafe_allow_html=True)
    elif 2.5 <= prediction <= 3.4:
        st.markdown('<div class="prediction-result">‚öΩ Prediction: Away Team Win</div>', unsafe_allow_html=True)
    else:
        st.markdown('<div class="prediction-result">‚ùó Prediction: Invalid prediction value</div>', unsafe_allow_html=True)

    # Display probabilities
    st.markdown('<div style="font-size: 20px; color: #FF4500; font-weight: bold; text-align: center;">Historical Probabilities:</div>', unsafe_allow_html=True)
    for result, prob in probabilities.items():
        st.markdown(f'<div style="font-size: 18px; text-align: center;">{result}: {prob:.2f}%</div>', unsafe_allow_html=True)

    # Additional insights
    st.markdown("""
        <div style="font-size: 18px; color: #32CD32; text-align: center;">Factors influencing the prediction include:
        - Head-to-head historical statistics
        </div>
    """, unsafe_allow_html=True)

